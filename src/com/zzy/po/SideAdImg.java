package com.zzy.po;

public class SideAdImg {
	private Integer id;
	private SideAd sidead;
	private String imgName;
	private String path;
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public SideAd getSidead() {
		return sidead;
	}

	public void setSidead(SideAd sidead) {
		this.sidead = sidead;
	}

	public String getImgName() {
		return imgName;
	}

	public void setImgName(String imgName) {
		this.imgName = imgName;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
}
